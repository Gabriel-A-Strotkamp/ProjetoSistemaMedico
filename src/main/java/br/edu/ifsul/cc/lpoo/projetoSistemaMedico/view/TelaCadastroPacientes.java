    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(paciente == null){
            paciente = new Pacientes();
        }

        paciente.setNome(txtNome.getText());
        paciente.setCpf(txtCpf.getText());
        paciente.setDoenca(txtDoenca.getText());
        paciente.setIdade(Integer.parseInt(txtIdade.getText()));

        jpa.conexaoAberta();
        try {
            jpa.persist(paciente);
            System.out.println("Medico cadastrada com sucesso");
        } catch (Exception ex) {
            Logger.getLogger(TelaPacientes.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro ao persistir paciente: "+paciente+" \n Erro: "+ex);
        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

