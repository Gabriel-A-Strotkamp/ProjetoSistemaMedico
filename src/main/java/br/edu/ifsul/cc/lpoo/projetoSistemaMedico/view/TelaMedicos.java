    private void btnRemoverMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverMedicoActionPerformed
        Medicos medicoSel = lstMedicos.getSelectedValue();
        if (medicoSel != null) {
            System.out.println("Medico: " + medicoSel.getId());
            try {
                jpa.conexaoAberta();

                int delOp = JOptionPane.showConfirmDialog(this,
                    "Tem certeza que deseja remover " + medicoSel.getNome() + "?");
                if (delOp == JOptionPane.YES_OPTION) {
                    jpa.remover(medicoSel);
                }

                jpa.fecharConexao();
                carregarMedicosCadastrados();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                    "Erro ao remover medico " + medicoSel + "\n" + e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um medico para remover");
        }
    }//GEN-LAST:event_btnRemoverMedicoActionPerformed

