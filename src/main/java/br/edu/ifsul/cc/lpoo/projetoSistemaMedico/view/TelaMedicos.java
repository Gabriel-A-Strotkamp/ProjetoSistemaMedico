    private void btnEditarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarMedicoActionPerformed
        Medicos medicoSel = lstMedicos.getSelectedValue();
        if (medicoSel != null) {
            TelaCadastroMedicos telaEdt = new TelaCadastroMedicos(this, rootPaneCheckingEnabled);
            telaEdt.setMedico(medicoSel);
            telaEdt.setVisible(true);

            carregarMedicosCadastrados();

        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Medico para Editar");
        }
    }//GEN-LAST:event_btnEditarMedicoActionPerformed

