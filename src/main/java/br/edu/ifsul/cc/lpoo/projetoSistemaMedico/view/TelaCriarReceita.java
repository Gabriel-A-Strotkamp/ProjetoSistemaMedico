    private void txtBuscaMedicamentoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaMedicamentoKeyReleased
        if(txtBuscaMedicamento.getText().trim().isEmpty()){
            carregarMedicamentosCadastrados();
        } else{
            jpa.conexaoAberta();
            DefaultListModel modeloLista = new DefaultListModel();
            modeloLista.addAll(jpa.getMedicamentos(txtBuscaMedicamento.getText().trim()));
            System.out.println("Medicamentos carregados: "+jpa.getMedicamentos(txtBuscaMedicamento.getText().trim()));
            lstMedicamentos.setModel(modeloLista);

            jpa.fecharConexao();
        }
    }//GEN-LAST:event_txtBuscaMedicamentoKeyReleased

