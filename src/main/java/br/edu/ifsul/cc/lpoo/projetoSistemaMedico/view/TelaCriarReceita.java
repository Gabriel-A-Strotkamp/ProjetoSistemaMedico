    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if(receita == null){
            receita = new Receituarios();
        }

        receita.setPaciente(paciente);
        receita.setMedico(medico);
        receita.setPrescricao(txtPrescricao.getText());
        

        //medico.addPaciente(paciente);
        
        jpa.conexaoAberta();
        
        try {
            jpa.persist(receita);
            System.out.println("Medico cadastrada com sucesso");
        } catch (Exception ex) {
            Logger.getLogger(TelaReceituarios.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro ao persistir Receitas: "+receita+" \n Erro: "+ex);
        }
        jpa.fecharConexao();
        
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

