    private void btnAddMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMedicamentoActionPerformed
        // TODO add your handling code here:
        Medicamentos medicamentoSel = lstMedicamentos.getSelectedValue();
        if (medicamentoSel != null) {
            
            receita.addMedicamento(medicamentoSel);
            
            carregarMedicamentosCadastrados();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Medicamento para adicionar");
        }
    }//GEN-LAST:event_btnAddMedicamentoActionPerformed

